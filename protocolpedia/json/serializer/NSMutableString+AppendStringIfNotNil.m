#import "NSMutableString+AppendStringIfNotNil.h"


//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
#pragma mark IMPLEMENTATION
//////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////

@implementation NSMutableString (AppendStringIfNotNil)

- (void)appendStringIfNotNil:(NSString *)aString {
	if (aString != nil)
		[self appendString:aString];
}

@end
